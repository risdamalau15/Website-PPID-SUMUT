            function valuemultiple(listarray, nameinput, data_array) {
                var namedatainput = '[id="' + nameinput + '"]';
                var listdata = listarray.map(function(group) {
                    return Array.from(group.querySelectorAll(namedatainput)).map(function(item) {
                        var value_date = $(item).attr("value_tanggal");
                        var date_all = get_event(item, value_date, data_array);
                        return date_all;
                    });
                });
                return listdata[0];
            }

            function get_event(element, date_all, data_array) {




                var list_red_date = [];

                for (var i = 0; i < data_array.length; i++) {
                    var red_date = data_array[i]['date'];
                    var task_moment = moment(red_date, 'YYYY-mm-DD').format('DD-mm-YYYY');
                    list_red_date.push(task_moment);


                }


                if (jQuery.inArray(date_all, list_red_date) != -1) {
                    $(element).addClass('red_date');
                }




            }



